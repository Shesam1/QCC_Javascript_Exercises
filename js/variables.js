/**
 * in javascript we create a variable using the var keyword
 *
 * we can use the following characters to name a variable
 * 
 * letters	a-z A-Z
 * digits   0-9
 * dollar   $
 * underscore _
 *
 * note! variable can't start with a digit
 */

var username = "Sherill Samuel";

// or can also be written as:
// var username;
// username = "Sherill Samuel";

console.log(username);

var firstname, lastname, points;

// *these three variables have undefined value unless we give it a value*

console.log(firstname);
console.log(lastname);
console.log(points);

var username;

console.log(username);

username = "Some New Value";

// or can also be written as:
// var username = "Some New Value";

console.log(username);